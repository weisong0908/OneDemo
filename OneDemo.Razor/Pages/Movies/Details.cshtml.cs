using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;
using Microsoft.AspNetCore.Mvc;
using Microsoft.AspNetCore.Mvc.RazorPages;
using Microsoft.EntityFrameworkCore;
using OneDemo.Razor.Models;
using OneDemo.Razor.Persistence;

namespace OneDemo.Razor.Pages.Movies
{
    public class DetailsModel : PageModel
    {
        private readonly OneDemo.Razor.Persistence.MovieContext _context;

        public DetailsModel(OneDemo.Razor.Persistence.MovieContext context)
        {
            _context = context;
        }

        public Movie Movie { get; set; }

        public async Task<IActionResult> OnGetAsync(int? id)
        {
            if (id == null)
            {
                return NotFound();
            }

            Movie = await _context.Movies.FirstOrDefaultAsync(m => m.Id == id);

            if (Movie == null)
            {
                return NotFound();
            }
            return Page();
        }
    }
}
